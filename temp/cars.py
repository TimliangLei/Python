cars=['bmw','audi','toyota','subaru']

print("倒排序");
cars.sort()
print(cars)

print("整排序");
cars.sort(reverse=True)
print(cars)

print("\nHere is the original list:")
print(cars)

print("\nHere is the sorted list:")
print(sorted(cars))

print("\nHere is the original list again:")
print(cars)
print(cars)
cars.reverse()
print(cars)
cars.reverse()
print(cars)
print(len(cars))
